    /**
     * Store a newly created resource in storage.
     */
    public function store(Request $request)
    {
        $request->validate([
            'name' => 'required',
            'subdomain' => 'required',
        ]);

        // dd($request->all());

        School::create(
            [
                'name' => $request->name,
                'subdomain' => $request->subdomain,
                'domain' => $request->domain,
            ]
        );

        return redirect()->route('schools.index')->with('success', 'School Details Save Success Fully!');
    }

